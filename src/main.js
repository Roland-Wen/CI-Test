/**
 * Add stuff
 * @param {int} a - input 1
 * @param {int} b - input 2
 * @return {int} a+b
 */
function add(a, b) {
  return a + b;
}

/**
 * Multiply stuff
 * @param {int} a - input 1
 * @param {int} b - input 2
 * @return {int} a*b
 */
function multiply(a, b){
  return a * b;
}

module.exports = {
  "add": add,
  "multiply": multiply,
}
